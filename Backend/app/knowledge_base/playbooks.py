import re

# Fine-grained detection: message-level substrings mapped to a specific
# playbook key, checked before falling back to the broader category-level
# playbook. Order matters — more specific patterns first.
_SUBTYPE_MARKERS = [
    ("instagram_hacked", ["instagram is hacked", "instagram hacked", "hacked my instagram"]),
    ("whatsapp_hacked", ["whatsapp is hacked", "whatsapp hacked", "hacked my whatsapp"]),
    ("email_hacked", ["gmail hacked", "email hacked", "email is hacked", "hacked my email", "hacked my gmail"]),
    ("sim_swap", ["sim swap", "sim card stopped", "sim stopped working", "duplicate sim"]),
    ("ransomware", ["ransomware", "files encrypted", "ransom demand", "ransom note"]),
    ("qr_scam", ["qr code", "scan qr", "scanned a qr"]),
    ("fake_loan_app", ["loan app", "loan harassment", "instant loan app"]),
    ("job_scam", ["job scam", "fake job offer", "registration fee for job", "work from home scam"]),
    ("investment_scam", ["crypto scam", "fake investment", "ponzi", "trading app scam", "guaranteed returns"]),
    ("upi_fraud", ["upi", "gpay", "phonepe", "google pay fraud"]),
    ("malware", ["malware", "virus on my", "device infected", "spyware", "keylogger", "anydesk", "teamviewer"]),
    ("deepfake", ["deepfake", "ai voice", "voice clone", "cloned voice", "fake video of me"]),
    ("cyberbullying", ["bully", "bullying", "mocking", "trolling", "spreading rumors", "harass"]),
    ("child_online_abuse", ["my child", "my kid", "my son", "my daughter", "grooming", "minor", "underage"]),
]

# Sextortion needs flexible matching, not rigid phrases — real messages say
# things like "leak my private photos" or "posted my nude pics", not the
# exact literal strings "leak my photo" / "revenge porn". Checked first,
# before the generic subtype list, since it's high-severity and should not
# be missed due to phrasing variation.
_SEXTORTION_RE = re.compile(
    r"\bsextortion\b|\brevenge porn\b|\bmorphed (?:photo|picture|video)\b"
    r"|\b(?:leak\w*|post\w*|shar\w*|publish\w*|expos\w*|send\w*)\b.{0,30}\b(?:photo|photos|pic|pics|picture|pictures|video|videos|nude|nudes|intimate)\b",
    re.IGNORECASE,
)


def match_playbook(message: str, category_value: Optional[str] = None) -> Optional[Playbook]:
    """Finds the most specific applicable playbook for a message, falling
    back to a category-level match for a few unambiguous categories."""
    text = message.lower()

    if _SEXTORTION_RE.search(text):
        return PLAYBOOKS.get("sextortion")

    for key, markers in _SUBTYPE_MARKERS:
        if any(m in text for m in markers):
            return PLAYBOOKS.get(key)

    category_fallback = {
        "sexually_explicit_content": "sextortion",
        "cyberbullying": "cyberbullying",
        "child_exploitation": "child_online_abuse",
    }
    if category_value in category_fallback:
        return PLAYBOOKS.get(category_fallback[category_value])
    return None


def build_playbook_block(playbook: Optional[Playbook]) -> str:
    """Formats a playbook as a prompt-ready reference block. Empty string
    if no playbook matched (model falls back to general knowledge-base
    guidance in that case)."""
    if playbook is None:
        return ""
    lines = [f"MATCHED INCIDENT PLAYBOOK — {playbook.name} (use this as your authoritative action reference, adapt wording to the conversation but don't contradict it):"]
    if playbook.immediate_actions:
        lines.append("Immediate actions: " + " | ".join(playbook.immediate_actions))
    if playbook.next_24_hours:
        lines.append("Next 24 hours: " + " | ".join(playbook.next_24_hours))
    if playbook.evidence_to_preserve:
        lines.append("Evidence to preserve: " + ", ".join(playbook.evidence_to_preserve))
    if playbook.prevention:
        lines.append("Prevention: " + " | ".join(playbook.prevention))
    return "\n".join(lines)
