"""
Specialized Incident Playbooks.

Each playbook is a curated, ordered set of actions for a specific incident
type. These are injected into the LLM prompt as authoritative reference
material (same trust tier as the legal knowledge base) so the model's
"Immediate Actions" / "Next 24 Hours" / "Evidence to Preserve" sections are
grounded in reviewed guidance rather than improvised each time — while the
actual prose is still generated by the model so it can adapt to the
specifics of the case.

Playbooks are looked up via message-text markers first (fine-grained,
e.g. "Instagram hacked" vs "WhatsApp hacked"), falling back to a coarse
category-level match for a few unambiguous categories.
"""

